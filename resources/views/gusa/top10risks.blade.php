                <h5 class="shortcode_toggles_item_title expanded_no" data-count="3">
                    3. Diabetes (Type 2)
                    <span class="ico"></span>
                </h5>

                <div class="shortcode_toggles_item_body">
                    <div class="ip">

                        <div class="darkgreytext">
                            <ul>

