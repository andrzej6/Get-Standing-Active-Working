                <h5 class="shortcode_toggles_item_title expanded_no" data-count="8">
                    8. Muscle Degeneration
                    <span class="ico"></span>
                </h5>

                <div class="shortcode_toggles_item_body">
                    <div class="ip">

                        <div class="darkgreytext">

                            <ul>
                                <li>
                                    Age-related declines in muscle mass and strength (sarcopenia) often have a
                                    negative impact on the independence of the elderly, increasing their risk of falling.
                                </li>

                                <li>
                                    When sedentary our locomotor muscles are inactive most of the day.
                                </li>

                                <li>
                                    Older adults watching TV at least 6 hours a day might have poorer
                                    grip strength compared to those who engage in less than 2 hours of TV watching a day (44).
                                </li>

                                <li>
                                    Stair climbing, for example, results in greater muscle activity than
                                    brisk walking, as do standing and walking compared with sitting (45).
                                </li>
                            </ul>

                        </div>

