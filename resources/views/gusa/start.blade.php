        <h1>How to Get Started</h1>

        <strong>If you're preparing to start sit-stand, congratulations!</strong> <br/><br/>

        We recommend you take it easy initially and build up your standing time gradually over a series of
        weeks and months.<br/><br/>

        Experts recommend splitting your time evenly between standing and sitting, accumulating at least
        <u>two-hours per day</u> of standing and light activity,
        eventually progressing to a total accumulation of <u>four hours</u>.<br/><br/>

        Those individuals new to adopting more standing-based work could expect some musculoskeletal
        sensations and some fatigue as part of the change process. If such sensations cannot be relieved
        either by an altered posture or walking for a few minutes, then you should rest (including sitting)
        with a posture that relieves the sensations. If discomfort does persist, then seek appropriate medical advice.
        <br/><br/>

        Similar to the risks of prolonged static seated positions, so too should prolonged static standing
        postures be avoided.
        <br/><br/>

        Please keep in mind our top 5 tips below:
        <br/><br/><br/>


        <div class="bluetitle"> Top 5 tips for users of Sit-Stand: </div>

        <ol>
            <li><strong>Build it up gradually.</strong> Start with 20 minute standing, then sit for 40 minutes and
                repeat throughout the day</li>
            <li><strong>Avoid excessive sitting and standing.</strong> Keep moving around, take frequent breaks,
                and make a conscious
                effort to mix it up. Standing all day can lead to back, knee or foot problems.</li>
            <li>Make sure you have <strong>comfortable footwear</strong> and think about a comfort mat.</li>
            <li>Find out what <strong>things you prefer doing standing </strong>e.g phone calls, clearing out
                your inbox, routine deskwork.</li>
            <li>Also find out the <strong>things you do better sitting </strong>e.g concise and creative writing,
                proof reading, in depth analysis.</li>
