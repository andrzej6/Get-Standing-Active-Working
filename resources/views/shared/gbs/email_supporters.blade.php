    <div style="font-size:0.8em;text-align:justify">
        <span style="font-weight:bold;color:#EA1E30">Get Britain Standing:</span> a campaign to grow awareness
        and education of the dangers of sedentary working and in particular prolonged sitting time. The average
        <span style="color:#EA1E30">UK</span> office worker sits 10 hours each day, with almost 70% of sitting
        taking place at work. There is growing scientific evidence highlighting the multiple health risks
        (including cardiovascular disease, diabetes (type 2), certain cancers and mental health) caused by
        excessive and prolonged sitting. <br/><br/>

        <strong>Active Working CIC </strong>is leading the global insight and evidence based research on
        sedentary behaviour. We commissioned the first
        <a href="http://activeworking.com/expert_statement.php">global expert recommendations </a>
        on standing time for office workers (with the support of <strong>Public Health England</strong>),
        published by the <strong>British Journal for Sports Medicine</strong> in June 2015. We spearhead
        international  <strong>Get Standing &trade;</strong> campaigns in <span style="color:#EA1E30">UK</span>, Australia, USA, Canada, Ireland and Europe.
