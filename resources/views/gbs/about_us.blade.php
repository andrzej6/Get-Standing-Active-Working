                    <h1>About Get Britain Standing</h1>

                    <br/>

                    Whilst visiting a charity in Gothenburg, Sweden in 2013, Gavin Bradley was curious
                    to understand why all the staff had access to Sit-Stand work stations – and what’s more they thought nothing of it.<br/><br/>

                    Puzzled by this, Gavin decided to investigate the background to why Sweden (and indeed Scandinavia) is all Sit-Standing
                    at work and he soon realised that there was some stark scientific research evidence which has resulted in
                    legislation providing guidance to employers that office workers should have a right to height adjustable
                    desks to reduce sitting time whilst at work.  <br/><br/>

                    Gavin decided that other countries needed simple (but stern) communication from the fast emerging area of
                    sedentary science. <br/><br/>

                    <strong>"In short we have dropped the ball and need to become more acutely aware of just how dangerous and widespread
                        the health risks of prolonged sitting can be - irrespective of the levels of physical activity".</strong><br/><br/>

                    Hence GET BRITAIN STANDING was born<br/><br/>


                    <div>
                        Our goal is to draw attention to the significant increase in health risks caused by sitting (i.e. heart disease,
                        diabetes and cancer) and compelling benefits of converting sitting time to standing time whilst at work
