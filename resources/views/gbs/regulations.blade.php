        <h1 class="activeworking">Regulations</h1>

        Most legislation covering computers is to be found in the Health and Safety (Display Screen Equipment)
        Regulations (1992), and in the 1993 Implementation of European Directive (90/270/EEC) on
        display screen work.<br/><br/>

        These regulations have been designed to help people who use computers as a significant part of
        their job.<br/><br/><br/>

        <i><strong>Employers Obligations:</strong></i><br/><br/>

        <div class="unorderedl">
            <ul>
                <li><strong>Provide furniture that can be adjusted.</strong> No two people are the same size or shape. Chairs, desks and monitors
                    need to be adjustable so that the user can alter the height,<br> angle of back rest etc. to reduce the risk
                    of backache, neck ache and other aches and pains
                </li>

                <li><strong>Make sure computer users take regular breaks.</strong> Working at a computer for long periods can make the user very
                    tired and can contribute to headaches, back and neck ache, Repetitive Strain Injury (see below).
                </li>

                <li><strong>Provide regular eyesight tests.</strong> Computers can increase the risk of eyestrain so users need to have regular
                    sight tests to keep their eyes healthy.
                </li>

                <li><strong>Provide information on health and safety.</strong> Most people do not know what the health and safety regulations
                    say; an employer must provide this information for their employees.
                </li>

                <li><strong>Assess the risks of using a particular workstation, and correct any problems. </strong>
                    Not all computer systems meet the regulations.<br> Some have monitors that cannot be adjusted, and some are used in
                    hazardous areas in factories. All computer equipment has to be risk assessed and made as safe as possible for the user.
                </li>

                <li><strong>Reduce the risks of conditions such as Repetitive Strain Injury. </strong>
                    Repetitive Strain Injury (RSI) is a term that covers a lot of complaints. Most of them are caused by a person
                    repeating an action over and over again. This is true for people who work on a single machine in a factory for
                    a long time. It is also true for computer users. RSI generally affects the hands, wrists and arms,
                    through repeated use whilst typing. RSI can cause damage to the joints and bones and in some cases the muscles.
                </li>
            </ul>


