        <h1>Expert Statement on the Sedentary Office</h1>




        <div class="expert-content">


            BRITISH JOURNAL OF SPORTS MEDICINE <br/>
            <img src="img/general/bmj.png"/>
            <br/>



            An expert statement jointly commissioned by <span class="blue-bold-underlined">Active Working CIC</span>
            and <strong>Public Health England</strong> (PHE) published in June 2015 by the
            <span class="blue-bold-underlined">British Journal of Sports Medicine</span>
            <strong>(BJSM)</strong>.<br/><br/>

            <div class="blue-italics">This is the first recommendation globally on curbing health risks of
                too much sitting at work.</div><br/>

            "For those working in offices, 65 - 75% of their working hours are spent sitting, of
            which more than 50% of this is accumulated in prolonged periods of sustained sitting".
            <br/><br/>

            "The evidence is clearly emerging that a first 'behavioural' step could be simply to get people
            standing and moving more frequently as part of their working day”, they say, adding that this
            is likely to be more achievable than targeted exercise.

            <br/><br/>

            <strong>Based on the current evidence Active Working recommends:</strong>



            <br/><br/>
            <ul class="show-bullets">
                <li>2 hours daily of standing and light activity (light walking) during working hours, eventually progressing to
                    a total of 4 hours for all office workers whose jobs are predominantly desk based </li>

                <li>Regularly breaking up seated based work with standing based work, with the use of adjustable sit-stand
                    desks/work stations</li>

                <li>Avoidance of prolonged static standing, which may be as harmful as prolonged sitting</li>

                <li>Altering posture/light walking to alleviate possible musculoskeletal  pain and fatigue as part of
                    the adaptive process</li>

                <li>As well as encouraging staff to embrace other healthy behaviours, such as cutting
                    down on drinking and smoking, eating a nutritious diet, and alleviating stress,
                    employers should also warn their staff about the potential dangers of too much
                    time spent sitting down either at work or at home</li>
            </ul>


            <br/>
            Some companies have already invested time and money creating a more active working environment
            for their staff, but those that haven’t should evaluate how best to achieve the recommendations.
            This could include deciding when and how staff take breaks which involve standing and
            movement;  and desk designs and technologies that allow employees to do their job more
            easily either at their desk or from other locations in the office while standing up.<br/><br/>

            The authors acknowledge that much of the evidence they draw on for their recommendations
            is based on observational and retrospective studies, which make it difficult to prove
            direct cause and effect. Nevertheless, they emphasise: "While longer term intervention
            studies are required, the level of consistent evidence accumulated to date, and the
            public health context of rising chronic diseases, suggest initial guidelines are justified."
